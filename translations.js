// Translate a feature by its checkbox value key (e.g., 'responsive_design')
function tFeature(key) {
  var translationKey = FEATURE_TRANSLATION_KEYS[key];
  if (translationKey) return t(translationKey);
  // Fallback: try feature.* short keys
  var shortKey = 'feature.' + key;
  var result = t(shortKey);
  if (result !== shortKey) return result;
  // Last fallback: humanize the key
  return key.replace(/_/g, ' ').replace(/\b\w/g, function(l) { return l.toUpperCase(); });
}

// Reverse map: English label → feature key (built lazily from EN translations)
var _reverseFeatureMap = null;
function _buildReverseFeatureMap() {
  if (_reverseFeatureMap) return _reverseFeatureMap;
  _reverseFeatureMap = {};
  Object.keys(FEATURE_TRANSLATION_KEYS).forEach(function(featureKey) {
    var enLabel = TRANSLATIONS['en'][FEATURE_TRANSLATION_KEYS[featureKey]];
    if (enLabel) {
      _reverseFeatureMap[enLabel.toLowerCase().trim()] = featureKey;
    }
  });
  // Also add e-commerce labels
  _reverseFeatureMap['no e-commerce'] = 'ecommerce_none';
  _reverseFeatureMap['basic e-commerce setup'] = 'ecommerce_basic_cart';
  _reverseFeatureMap['full e-commerce store'] = 'ecommerce_full_store';
  return _reverseFeatureMap;
}

// Translate a stored feature label (from Firestore) to current language
function translateStoredFeature(label) {
  if (!label) return label;
  var reverseMap = _buildReverseFeatureMap();
  var key = reverseMap[label.toLowerCase().trim()];
  if (key) {
    // Handle e-commerce keys
    if (key === 'ecommerce_none') return t('ecommerce.none');
    if (key === 'ecommerce_basic_cart') return t('ecommerce.basicCart');
    if (key === 'ecommerce_full_store') return t('ecommerce.fullStore');
    return tFeature(key);
  }
  return label; // Fallback to original label
}

// Translate addon/discount by key (handles ecommerce_ prefix)
function translateAddonByKey(key, fallbackLabel) {
  if (!key) return fallbackLabel || '';
  if (key.indexOf('ecommerce_') === 0) {
    var ecomKey = key.replace('ecommerce_', '');
    if (ecomKey === 'none') return t('ecommerce.none');
    if (ecomKey === 'basic_cart') return t('ecommerce.basicCart');
    if (ecomKey === 'full_store') return t('ecommerce.fullStore');
  }
  return tFeature(key);
}

